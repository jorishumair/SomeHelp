    public static void DialogMessage(Context mContext, String text, boolean bAutoClose, int iDuration, boolean bYesButton, boolean bNoButton){

        final Dialog dialog = new Dialog(mContext);
        dialog.requestWindowFeature(Window.FEATURE_NO_TITLE);
        dialog.setContentView(R.layout.dialog_simple);

        TextView txt_message = (TextView) dialog.findViewById(R.id.txt_message);
        Button btnOui = (Button) dialog.findViewById(R.id.btn_oui);
        Button btnNon = (Button) dialog.findViewById(R.id.btn_non);

        txt_message.setText(text);

        if(bAutoClose)
        {
            //btnOui.setText(mContext.getResources().getString(R.string.fermer));
            btnOui.setVisibility(View.GONE);
            btnNon.setVisibility(View.GONE);

            new Handler().postDelayed(new Runnable() {

                @Override
                public void run() {
                    dialog.dismiss();
                }
            }, iDuration);
        }else
        {
            btnOui.setOnClickListener(new View.OnClickListener() {
                @Override
                public void onClick(View view) {
                    dialog.dismiss();
                }
            });
        }
        dialog.show();
    }

